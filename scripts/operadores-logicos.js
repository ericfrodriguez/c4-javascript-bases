
//* Operador AND (&&)

true  && true  == true; // El unico caso en el que es verdadero.
true  && false == false;
false && true  == false;
false && false == false;

//* Operador OR (||)

true  || true  == true;
true  || false == true;
false || true  == true;
false || false == false; // Unico caso en el que es falso.

//* Operador NOT (!)

!true == false;
!false == true;